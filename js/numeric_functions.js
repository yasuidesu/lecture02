function add(valueA, valueB){
  return valueA + valueB;
}

function subtract(valueA, valueB){
  return valueA - valueB;
}

function multiply(valueA, valueB){
  return valueA * valueB;
}

function divide(valueA, valueB){
  return valueA / valueB;
}

function mod(valueA, valueB){
  return valueA % valueB;
}
